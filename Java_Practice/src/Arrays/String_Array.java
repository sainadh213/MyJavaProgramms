package Arrays;

public class String_Array {

	public static void main(String[] args) {
		String s[]= {"nadh","sai","Busam"};
		System.out.print("Printing of String Array :");
		 for(int i=0;i<s.length;i++)
		 {
			 System.out.print(s[i]+" ");
		 }
		 System.out.println();
		 System.out.print("printing of String Array in Reverse Order :");
		 for(int j=s.length-1;j>=0;j--)
		 {
			 System.out.print(s[j]+" ");
		 }
		 System.out.println();
		 
			}


	}


