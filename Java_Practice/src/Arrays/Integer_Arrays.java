package Arrays;

public class Integer_Arrays {

	public static void main(String[] args) {
		int a[]= {21,56,892,56,86};
		
		System.out.print("Printing of Integer Array :");
 for(int i=0;i<a.length;i++)
 {
	 System.out.print(a[i]+" ");
 }
 System.out.println();
 System.out.print("printing of Integer Array in Reverse Order :");
 for(int j=a.length-1;j>=0;j--)
 {
	 System.out.print(a[j]+" ");
 }
 System.out.println();
 
	}

}
